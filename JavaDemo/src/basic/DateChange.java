/*
 * 
 * 数据类型转换
 * 
 * */
package basic;

public class DateChange {

	public static void main(String[] args) {
		int i = 333889890;							//声明一个整型变量
		System.out.println("int i = "+i);			//输出整型变量i
		double j = i;								//声明一个双精度型变量d，并将i值赋给d
		System.out.println("double j = "+j);		//输出双精度型变量
		float k = i;								//声明一个浮点型变量f，并将i的值赋给f
		System.out.println("float k = "+k);			//输出浮点型变量
	}

}
